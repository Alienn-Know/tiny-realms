import type { World } from './World';

/**
 * ⚙️ База для систем ECS.
 *
 * Система — это чистая логика без собственного состояния: на каждом кадре
 * она берёт из {@link World} нужные компоненты и что-то с ними делает.
 *
 * Системы запускаются в порядке добавления через `world.addSystem(...)`.
 */
export abstract class System {
  /**
   * ▶️ Один шаг системы. Вызывается каждый кадр.
   * @param world - мир со всеми сущностями, компонентами и системами
   * @param dt - время с прошлого кадра, в секундах
   */
  abstract update(world: World, dt: number): void;
}
